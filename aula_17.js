// aprendendo sobre SWITCH CASE //

function teste(key) {
    switch (key) {
        case 'Gabriel':
            console.log('Gabriel Aqui')
            break;
        case 'Gustavo':
            console.log('Gustavo Aqui')
            break;
        case 'Gerlaine':
            console.log('Gerlaine Aqui')
            break;
        default:
            console.log('Qualque Outro Aqui')
            break;
    }
}

teste('Gabriel')